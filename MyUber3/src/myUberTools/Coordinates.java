package myUberTools;

public class Coordinates {

	public double longitude;
	public double latitude;
	
	protected double getLongitude() {
		return longitude;
	}
	protected void setLongitude(double x) {
		this.longitude = x;
	}
	protected double getLatitude() {
		return latitude;
	}
	protected void setLatitude(double y) {
		this.latitude = y;
	}
	
	
}