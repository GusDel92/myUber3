package supplyClasses;

public class Coordinates {

	public double x;
	public double y;
	
	protected double getX() {
		return x;
	}
	protected void setX(double x) {
		this.x = x;
	}
	protected double getY() {
		return y;
	}
	protected void setY(double y) {
		this.y = y;
	}
	
	
}